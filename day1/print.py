# print function to print to the console | print()

# when printing strings you can use single or double quotes
print("Hello World!")

# when starting to code, we think we need to do the above to print the same thing on individual lines.

print("Hello World!")
print("Hello World!")
print("Hello World!")

# to print on a new line use \n this will print the second Hello World on the new line.

print("Hello World!\nHello World!")

# the + is used for string concatenation, the combining of 2 strings

print("Hello " + "James")

# the input function allows the user to enter data in the console, when the code is run the cursor will be waiting for the users response

print("Hello " + input("What is your name?"))
